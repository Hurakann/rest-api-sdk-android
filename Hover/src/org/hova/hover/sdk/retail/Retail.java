package org.hova.hover.sdk.retail;

public class Retail {

	private String retail_ad_id;
	private String ad;
	private String title;

	public String getRetail_ad_id() {
		return retail_ad_id;
	}

	public void setRetail_ad_id(String retail_ad_id) {
		this.retail_ad_id = retail_ad_id;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

}
