package org.hova.hover.sdk.pojo;

public class Catalog {

	private String name;
	
	private String estate_id;
	
	private String country_id;
	
	private String country_code;
	
	private String town_id;
	
	private String type_site;
	
	private String iso_country_code;
	
	private int num_digits;

	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEstate_id() {
		return estate_id;
	}

	public void setEstate_id(String estate_id) {
		this.estate_id = estate_id;
	}

	public String getCountry_id() {
		return country_id;
	}

	public void setCountry_id(String country_id) {
		this.country_id = country_id;
	}

	public String getCountry_code() {
		return country_code;
	}

	public void setCountry_code(String country_code) {
		this.country_code = country_code;
	}

	public String getTown_id() {
		return town_id;
	}

	public void setTown_id(String town_id) {
		this.town_id = town_id;
	}

	public String getType_site() {
		return type_site;
	}

	public void setType_site(String type_site) {
		this.type_site = type_site;
	}

	public String getIso_country_code() {
		return iso_country_code;
	}

	public void setIso_country_code(String iso_country_code) {
		this.iso_country_code = iso_country_code;
	}

	public int getNum_digits() {
		return num_digits;
	}

	public void setNum_digits(int num_digits) {
		this.num_digits = num_digits;
	}
	
}

